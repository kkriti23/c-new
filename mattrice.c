//00*00+01*10+02*20    00*01+01*11+02*21    00*02+01*12+02*22
//10*00+11*10+12*20    10*01+11*11+12*21    10*02+11*12+12*22
//20*00+21*10+22*20    20*01+21*11+22*21    20*02+21*12+22*22
//i*k+k*j
#include<stdio.h>
int main()
{
	int i,j,k;
	for(i=0;i<3;i++){
		for(j=0;j<3;j++){
			for(k=0;k<3;k++){
				printf("%d%d*%d%d",i,k,k,j);
				if(k<2){
					printf("+");
				}
			}
			printf("\t");
		}
		printf("\n");
	}
	
}
